# в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ
# ##  VOID Stress iSH вҖ” launcher
# ##  sh ddos.sh   вҶҗ works on ash / iSH / Alpine
# в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ

DIR="$(cd "$(dirname "$0")" && pwd)"

R='\033[1;31m'
G='\033[1;32m'
Y='\033[1;33m'
C='\033[1;36m'
W='\033[1;37m'
D='\033[2m'
RST='\033[0m'
BLOOD='\033[38;5;160m'

printf '\n%b' "${BLOOD}"
printf 'в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ\n'
printf '   VOID STRESS вҖ” iSH Setup & Launch\n'
printf 'в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ\n'
printf '%b\n' "${RST}"

# в”Җв”Җ 1. python3 в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if ! command -v python3 >/dev/null 2>&1; then
  printf '%b  Installing python3...%b\n' "${Y}" "${RST}"
  apk add python3 --no-progress -q
fi
PY=$(python3 --version 2>&1)
printf '%bвң”%b  %s\n' "${G}" "${RST}" "$PY"

# в”Җв”Җ 2. pip3 в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if ! command -v pip3 >/dev/null 2>&1 && ! python3 -m pip --version >/dev/null 2>&1; then
  printf '%b  Installing pip...%b\n' "${Y}" "${RST}"
  apk add py3-pip --no-progress -q
fi
printf '%bвң”%b  pip ready\n' "${G}" "${RST}"

# в”Җв”Җ 3. python packages в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
install_pkg() {
  pkg="$1"
  python3 -c "import $pkg" >/dev/null 2>&1 && return 0
  printf '%b  Installing %s...%b\n' "${Y}" "$pkg" "${RST}"
  pip3 install "$pkg" -q 2>/dev/null \
    || python3 -m pip install "$pkg" -q 2>/dev/null \
    || { printf '%bвңҳ  Failed to install %s%b\n' "${R}" "$pkg" "${RST}"; return 1; }
  printf '%bвң”%b  %s installed\n' "${G}" "${RST}" "$pkg"
}

install_pkg rich
install_pkg requests

printf '\n%bв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ%b\n\n' "${BLOOD}" "${RST}"

# в”Җв”Җ 4. launch в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [ -f "${DIR}/ddos.py" ]; then
  python3 "${DIR}/ddos.py"
elif [ -f "${DIR}/void_stress_ish.py" ]; then
  python3 "${DIR}/void_stress_ish.py"
else
  printf '%bвңҳ  ddos.py not found in %s%b\n' "${R}" "${DIR}" "${RST}"
  exit 1
fi

